package Arrays;

public class arrays {
    public static void main(String[] args){
        int[][]Age={{13,12,7,18},{21,23,27,24}};
        for(int H=0; H< Age.length;++H){
            for(int y=0; y < Age[H].length; ++y){
                System.out.println(Age[H][y]);

            }
            //inner looping Arrays
        }
    }
}




